# Variable = værdi
myMessage = "Hello, World!"


# for-loop
for i in range(3):
    print(myMessage + " " + str(i))

# Branching if-statement if-else, if-elif-else
value = 52

if value < 50:
    print("Value is less than 50")
elif value < 60:
    print("Value is less than 60")
elif value < 70:
    print("Value is less than 70")
